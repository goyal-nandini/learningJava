package Strings;

public class PreetyPrinting {
    public static void main(String[] args) {

         // "%" is a placeholder
        System.out.printf("formatted no. is %.2f", 23.4566); // 23.46 (rounding off)
        System.out.println();
        System.out.printf("formatted no. is %.3f", Math.PI); // 3.141
        System.out.println();
        System.out.printf("My name is %s and i am %s", "nandini", "cool");
    }
}
